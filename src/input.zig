pub const MouseButtonEvent = ModInput(MouseButton);

pub const MouseButton = enum(c_int) {
    zero = 0,
    one = 1,
    two = 2,
    three = 3,
    four = 4,
    five = 5,
    size = 6,
    seven = 7,
};

pub const ButtonAction = enum(c_int) {
    release = 0,
    press = 1,
    repeat = 2,
};

pub const ActionModifiers = packed struct(u8) {
    shift: bool = false,
    control: bool = false,
    alt: bool = false,
    super: bool = false,
    caps_lock: bool = false,
    num_lock: bool = false,
    _padding: u2 = 0,
};

fn ModInput(comptime T: type) type {
    return struct {
        which: T,
        action: ButtonAction,
        modifiers: ActionModifiers,
    };
}

